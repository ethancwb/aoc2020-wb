from functools import reduce

def day3b(map):
  mapLst = map.split('\n')
  patternLen, treesList = len(mapLst[0]), [0] * 5
  for idx, line in enumerate(mapLst):
    for j in [1,3,5,7]:
      if line[idx * j % patternLen] == '#':
        treesList[j // 2] += 1
    if idx % 2 == 0 and line[(idx // 2) % patternLen] == '#':
      treesList[-1] += 1
  return reduce(lambda x, y: x * y, treesList)

def day3a(map):
  return sum(1 for idx, line in enumerate(map.split('\n')) if line[idx * 3 % len(line)] == '#')

